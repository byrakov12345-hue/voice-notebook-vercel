export function prepareSpeechText(text) {
  const replacements = [
    [/\b褋屑褋\b/gi, '褝褋 褝屑 褝褋'],
    [/\bsms\b/gi, '褝褋 褝屑 褝褋'],
    [/\bwhatsapp\b/gi, '胁邪褌褋邪锌'],
    [/\bapi\b/gi, '褝泄 锌懈 邪泄'],
    [/\bjson\b/gi, '写卸械泄褋芯薪'],
    [/\burl\b/gi, '褞 邪褉 褝谢'],
    [/\bid\b/gi, '邪泄 写懈'],
    [/\b泻芯写褘 懈 泻芯屑斜懈薪邪褑懈懈\b/gi, '泻芯写褘 懈 泻芯屑斜懈薪邪褑懈懈'],
    [/\b泻芯薪褌邪泻褌褘\b/gi, '泻芯薪褌邪泻褌褘'],
    [/\b胁褋褌褉械褔懈\b/gi, '胁褋褌褉械褔懈'],
    [/\b锌芯泻褍锌泻懈\b/gi, '锌芯泻褍锌泻懈'],
    [/\b蟹邪写邪褔懈\b/gi, '蟹邪写邪褔懈'],
    [/\b屑邪褕懈薪邪\b/gi, '屑邪褕懈薪邪'],
    [/\b褍褔褢斜邪\b/gi, '褍褔褢斜邪'],
    [/\b械械\b/g, '械褢'],
    [/\b衼\b/g, '衼']
  ];

  let value = String(text || '');
  for (const [pattern, replacement] of replacements) {
    value = value.replace(pattern, replacement);
  }

  return value
    .replace(/\n+/g, '. ')
    .replace(/\s{2,}/g, ' ')
    .replace(/,\s*/g, ', ')
    .replace(/:\s*/g, ': ')
    .trim();
}

export function getVoiceStyleConfig(style) {
  switch (style) {
    case 'male':
      return { label: '袦褍卸褋泻芯泄', rate: 0.9, pitch: 0.72 };
    case 'child':
      return { label: '袛械褌褋泻懈泄', rate: 1.02, pitch: 1.32 };
    case 'robot':
      return { label: '袪芯斜芯褌', rate: 0.86, pitch: 0.58 };
    default:
      return { label: '袨斜褘褔薪褘泄', rate: 0.92, pitch: 1 };
  }
}

export function speak(text, preferredVoiceURI = '', voiceStyle = 'default') {
  if (!window.speechSynthesis) return;
  window.speechSynthesis.cancel();
  const msg = new SpeechSynthesisUtterance(prepareSpeechText(text));
  msg.lang = 'ru-RU';
  const voices = window.speechSynthesis.getVoices?.() || [];
  const preferredVoice = preferredVoiceURI ? voices.find(voice => voice.voiceURI === preferredVoiceURI) : null;
  const ruVoice = preferredVoice || voices.find(voice => /^ru(-|_)?/i.test(voice.lang)) || voices.find(voice => /russian|褉褍褋/i.test(voice.name));
  if (ruVoice) msg.voice = ruVoice;
  const styleConfig = getVoiceStyleConfig(voiceStyle);
  msg.rate = styleConfig.rate;
  msg.pitch = styleConfig.pitch;
  window.speechSynthesis.speak(msg);
}

export function formatDate(iso) {
  try {
    return new Intl.DateTimeFormat('ru-RU', {
      day: '2-digit', month: '2-digit', year: 'numeric', hour: '2-digit', minute: '2-digit'
    }).format(new Date(iso));
  } catch {
    return '';
  }
}
